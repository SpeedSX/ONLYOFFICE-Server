function vkey(document_id) {
    var ms = Date.now().valueOf();
    var primary_key = "{\"expire\":\"\\/Date(" + ms + ")\\/\",\"key\":\"" + document_id + "\",\"key_id\":\"" + window.Config.EditorKeyId + "\"}";

    var md5_arr = SparkMD5.hash(primary_key + window.Config.EditorKeySecret, true);

    // get 16 bytes from 4 four-byte words
    var md5_chars = [
        (md5_arr[0]) & 0xff,
        (md5_arr[0] >>> 8) & 0xff,
        (md5_arr[0] >>> 16) & 0xff,
        (md5_arr[0] >>> 24) & 0xff,
        (md5_arr[1]) & 0xff,
        (md5_arr[1] >>> 8) & 0xff,
        (md5_arr[1] >>> 16) & 0xff,
        (md5_arr[1] >>> 24) & 0xff,
        (md5_arr[2]) & 0xff,
        (md5_arr[2] >>> 8) & 0xff,
        (md5_arr[2] >>> 16) & 0xff,
        (md5_arr[2] >>> 24) & 0xff,
        (md5_arr[3]) & 0xff,
        (md5_arr[3] >>> 8) & 0xff,
        (md5_arr[3] >>> 16) & 0xff,
        (md5_arr[3] >>> 24) & 0xff];

    // encode as array of bytes, not as a string!
    var secret_hash = encode_array_base64(md5_chars);

    var payload = secret_hash + "?" + primary_key;
    var data = Base64.encode(payload);

    var cnt = 0;
    while (data.indexOf('=') !== -1) {
        cnt++;
        data = data.replace('=', '');
    }

    data = (data + cnt).replace(/\+/g, '/').replace(/-/g, '_');

    return data;
}

function encode_array_base64(data) {
    var str = String.fromCharCode.apply(null, data);
    return btoa(str).replace(/.{76}(?=.)/g, '$&\n');
}

